/* ─── Data Models ─── */

export interface SeamAP {
  slug: string
  name: string
  photo: string
  title: string
  organization: string
  location: { city: string; state: string }
  specialties: string[]
  bio: string
  credentials: 'SEAM AP' | 'SEAM AP+' | 'Member'
  membershipTier?: 'Open' | 'Changemaker'
  roles?: ('Leadership' | 'TAG')[]
  linkedIn?: string
  website?: string
  dateCredentialed: string
  projectsLed: number
}

export interface CertifiedProject {
  slug: string
  name: string
  image: string
  location: { city: string; state: string }
  certificationLevel: 'Pursuing' | 'Endorsement' | 'Pre-certification' | 'Bronze' | 'Silver' | 'Gold' | 'Platinum'
  ratingSystem:
    | 'Buildings + Interiors'
    | 'Operations + Maintenance'
    | 'Community Development Endorsement'
    | 'Modern Slavery Assessment Endorsement'
  description: string
  completionDate: string
  size: string
  owner: string
  highlights: string[]
  apLead?: string
  blogPost?: string
  website?: string
}

export interface MemberOrg {
  slug: string
  name: string
  logo?: string
  tier: 'Founding Sponsor' | 'Member'
  description: string
  location: { city: string; state: string }
  website?: string
  sectors: string[]
}

export interface ApprovedActivity {
  slug: string
  name: string
  organization: string
  category:
    | 'Procurement'
    | 'Community Engagement'
    | 'Workforce'
    | 'Accessibility'
    | 'Health + Wellness'
    | 'Cultural Programming'
  pillar:
    | 'Social Impact'
    | 'Social Responsibility'
    | 'Social Justice'
    | 'Social Accountability'
  description: string
  dateApproved: string
  location: { city: string; state: string }
}

